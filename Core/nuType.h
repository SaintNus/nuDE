#ifndef __NUTYPE_H__
#define __NUTYPE_H__

//! \brief 8-bit integer.
typedef char i8;
//! \brief Unsigned integer.
typedef unsigned char ui8;

//! \brief 16-bit integer.
typedef short i16;
//! \brief Unsigned integer.
typedef unsigned short ui16;

//! \brief Integer.
typedef int32_t i32;
//! \brief Unsigned integer.
typedef uint32_t ui32;

//! \brief Float.
typedef float f32;
//! \brief Double.
typedef double f64;

//! \brief Boolean.
typedef bool boolean;

//! \brief String.
typedef char* cstr;
//! \brief String constant.
typedef const char* ccstr;

#endif
